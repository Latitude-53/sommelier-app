# Загружаем существующий словарь
with open(GLOSSARY_PATH, 'r', encoding='utf-8') as f:
    glossary = json.load(f)
print(f'Словарь: {len(glossary)} терминов')

# Загружаем drinks.json чтобы связать сорта с напитками
with open(DRINKS_PATH, 'r', encoding='utf-8') as f:
    drinks = json.load(f)

# Создаём grapes.json
grapes = []
removed_from_glossary = 0
for term, meta in GRAPES_TO_MOVE.items():
    # Если есть в словаре — берём определение оттуда, удаляем из словаря
    if term in glossary:
        description = glossary[term]
        del glossary[term]
        removed_from_glossary += 1
    else:
        description = meta.get('aroma', '')

    # Находим напитки в базе, связанные с этим сортом
    term_lower = term.lower().split(' (')[0].split(' /')[0]
    related_drinks = []
    for d in drinks:
        hay = (d.get('cat', '') + ' ' + d.get('name', '')).lower()
        if term_lower in hay:
            related_drinks.append(d['id'])

    grapes.append({
        'name': term,
        'color': meta['color'],
        'origin': meta['origin'],
        'climate': meta['climate'],
        'aroma': meta['aroma'],
        'structure': meta['structure'],
        'desc': description,
        'drink_ids': related_drinks
    })

# Сохраняем
with open(GRAPES_PATH, 'w', encoding='utf-8') as f:
    json.dump(grapes, f, ensure_ascii=False, indent=2)
print(f'Создан {GRAPES_PATH}: {len(grapes)} сортов')

# Сохраняем обновлённый словарь
with open(GLOSSARY_PATH, 'w', encoding='utf-8') as f:
    json.dump(glossary, f, ensure_ascii=False, indent=2)
print(f'Словарь после очистки: {len(glossary)} терминов (удалено {removed_from_glossary})')
