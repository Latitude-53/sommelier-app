# ============== DATA FILES (admin-editable JSON) ==============
DATA_DIR = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), 'data')

def save_data_to_json():
    """Export current Python data to JSON files in data/ folder."""
    os.makedirs(DATA_DIR, exist_ok=True)
    # Strip non-serializable fields (lambdas in quiz filters)
    def strip_lambdas(obj):
        if isinstance(obj, dict):
            return {k: strip_lambdas(v) for k, v in obj.items() if not callable(v)}
        if isinstance(obj, list):
            return [strip_lambdas(x) for x in obj]
        return obj
    files = {
        'drinks.json': DRINKS,
        'glossary.json': GLOSSARY,
        'taxonomy.json': TAXONOMY,
        'quiz.json': strip_lambdas(QUIZ),
        'dish_pairs.json': DISH_PAIRS,
        'blind_modes.json': BLIND_MODES,
    }
    for fname, data in files.items():
        path = os.path.join(DATA_DIR, fname)
        with open(path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
    print(f'Wrote {len(files)} JSON files to {DATA_DIR}/')

